num = float(input('Digite um número: '))
antecessor = num - 1
sucessor = num + 1
dobro = num * 2
triplo = num * 3
raiz = num ** (1/2)
print(f'O antecessor de {num} é {antecessor:.1f}')
print(f'O sucessor de {num} é {sucessor:.1f}')
print(f'O dobro de {num} é {dobro:.1f}')
print(f'O triplo de {num} é {triplo:.1f}')
